package excercises;


public class Variables {
    public static void main(String[] args) {

        //Create a variable of type int, name it whatever you want, and assign the value of 5
        int num = 5;


        //Print out the created variable in the console
        System.out.print(num);

        //anything in double quotes are called string literals
        System.out.print("num");

        //reassigning a variable
        num = 3;

        //printing new variable
        System.out.print(num);

        //below is a mathmatical expression and so it will add 10 and 5
        num = 10 + 5;

        System.out.print(num);


    }
}
